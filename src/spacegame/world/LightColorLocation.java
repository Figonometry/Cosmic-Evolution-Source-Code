package spacegame.world;

public final class LightColorLocation {
    public final int x;
    public final int y;
    public final int z;
    public final int colorValue;

    public LightColorLocation(int x, int y, int z, int colorValue) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.colorValue = colorValue;
    }
}
